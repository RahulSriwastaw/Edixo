import * as React from "react";
import { motion } from "framer-motion";
import { cn } from "../../../lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { hover?: boolean }
>(({ className, hover = true, ...props }, ref) => {
  const { onDrag, onDragStart, onDragEnd, ...rest } = props as any;
  return (
    <motion.div
      ref={ref}
      whileHover={hover ? { y: -2, boxShadow: "0 10px 25px -5px rgba(0, 0, 0, 0.05), 0 8px 10px -6px rgba(0, 0, 0, 0.05)" } : {}}
      transition={{ duration: 0.2 }}
      className={cn(
        "rounded-2xl border border-[#E5E7EB] bg-white text-slate-900 shadow-sm",
        className
      )}
      {...rest}
    />
  );
});
Card.displayName = "Card";

